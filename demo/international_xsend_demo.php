    /*
     |载入 app_config 文件
     |--------------------------------------------------------------------------
     */
    require '../app_config.php';
    
    /*
     |载入 SUBMSGAutoload 文件
     |--------------------------------------------------------------------------
     */
    
    require_once('../SUBMSGAutoload.php');
    
    /*
     |初始化 MESSAGEXsend 类
     |--------------------------------------------------------------------------
     */
    
    $submsg=new MESSAGEXsend($message_configs);
    /*
     |必选参数
     |--------------------------------------------------------------------------
     |设置国家区域代码  不要有'+'号 如 93  该接口不支持 86（中国地区）的发送
     |--------------------------------------------------------------------------
     */
	
	 $submsg->SetRegionCode('93');  
    /*
     |必选参数
     |--------------------------------------------------------------------------
     |设置国际短信的号码
     |--------------------------------------------------------------------------
     */

	
    $submsg->setTo('2254625');
    
    /*
     |必选参数
     |--------------------------------------------------------------------------
     |设置短信模板ID
     |--------------------------------------------------------------------------
     */
    
    $submsg->SetTempid('KLOJH');
    
    /*
     |可选参数
     |--------------------------------------------------------------------------
     |添加文本变量
     |可多次调用
     |--------------------------------------------------------------------------
     */
    
    $submsg->AddVar('code','198278');
    
    /*
     |调用 xsend 方法发送短信
     |--------------------------------------------------------------------------
     */
    
    $xsendInternational=$submsg->xsendInternational();
    
    
    /*
     |打印服务器返回值
     |--------------------------------------------------------------------------
     */
    
    print_r($xsendInternational);
	
	
